function calculatorMaxProPlusDeluxeVIPEdition(a, b, operation) {
    switch (operation) {
        case '+':
            return a + b;
        case '-':
            return a - b;
        case '*':
            return a * b;
        case '/':
            return a / b;
    }
}

export default calculatorMaxProPlusDeluxeVIPEdition();